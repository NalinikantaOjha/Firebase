package com.example.firebasetwo;

public class Student {


    private String name;
    private int rol;

    Student() {

    }

    public Student(String name, int rol) {
        this.name = name;
        this.rol = rol;
    }

    public String getName() {
        return name;
    }

    public int getRol() {
        return rol;
    }
}

